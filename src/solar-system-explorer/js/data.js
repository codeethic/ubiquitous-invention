// Earth orbital speed is the baseline: 2π / 60 seconds at 1× speed
// Other planet speeds are derived from real orbital period ratios.
export function getOrbitalSpeed(body) {
    if (body.orbitalPeriodDays <= 0) return 0;
    const earthBaseSpeed = (2 * Math.PI) / 60; // Earth completes orbit in 60s at 1×
    return earthBaseSpeed * (365.25 / body.orbitalPeriodDays);
}
